struct phy_parameters {
        unsigned int min_hz;
        unsigned int max_hz;
        unsigned int clock_amplitude;
        unsigned int data_amplitude;
        unsigned int pre_emphasis;
        unsigned int termination;
};
struct phy_custom_parameters_t {
        unsigned int valid;
        /* TX Clock Channel Amplitude Control
         * 00000 = 4mA, 11111 : 19.5mA, 0.5mA step. Default = 10000 (12mA)
         */
        unsigned int clock_amplitude;
        /* TX DATA Channel Amplitude Control
         * 00000 = 4mA, 11111 : 19.5mA, 0.5mA step. Default = 10000 (12mA)
         */
        unsigned int data_amplitude;
        /* TX Data Channel Pre-emphasis Control
         *  0000 = 0mA, 1111= 7.5mA, step=0.5mA */
        unsigned int pre_emphasis;
        /* TX Source Termination Resistor Control
         * 00 : OPEN, 01 :300Ohm, 10 : 150Ohm, 11 : 100Ohm
         */
        unsigned int termination;
};


