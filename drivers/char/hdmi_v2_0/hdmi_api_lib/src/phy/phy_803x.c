void tcc_hdmi_phy_standby(struct hdmi_tx_dev *dev)
{
        if(dev != NULL && dev->ddibus_io != NULL) {
                // 0. i_presetn = 1
                VIOC_DDICONFIG_reset_hdmi_phy(dev->ddibus_io, 0);
                // 1. clear mode set done of hdmi phy
                tcc_hdmi_phy_mode_set_done(dev, 0);
                // 2. i_phy_rstn = 0
                mc_phy_reset(dev, 0);
                // 3. i_phy_en = 0
                hdmi_phy_gen2_tx_power_on(dev, 0);
        }
}

int tcc_hdmi_phy_config(struct hdmi_tx_dev *dev, unsigned int pixel_clock, unsigned int tmds_clock, color_depth_t color_depth, int scdc_present, int enable_scramble)
{
        int ret = 0;
        int phy_loop;
        int phylist_max;
        unsigned char phy_val;
        unsigned char *phy_regs = NULL;
        unsigned char phy_tune_regs[HDMI_PHY_REGS];
        unsigned int phy_offset;

        //pr_info("%s %d %d %d %d %d\r\n", __func__, pixel_clock, tmds_clock, color_depth, scdc_present, scdc_lte_340);
        phylist_max = sizeof(tcc_phy_config_regs)/sizeof(struct hdmi_phy_configs);

        for(phy_loop=0; phy_loop <  phylist_max; phy_loop++){
                if(pixel_clock == tcc_phy_config_regs[phy_loop].hz && color_depth == (color_depth_t)tcc_phy_config_regs[phy_loop].pixel) {
                        memcpy(phy_tune_regs, &tcc_phy_config_regs[phy_loop].reg, HDMI_PHY_REGS);
                        phy_regs = phy_tune_regs;
                        break;
                };
        }
        if(phy_regs == NULL) {
                printk(KERN_ERR "[ERROR][HDMI_V20]%s with %dHz %d pixel is failed\r\n", __func__, pixel_clock, color_depth);
                ret = -1;
                goto end_process;
        }

        tcc_hdmi_phy_tuning(pixel_clock, (color_depth_t)color_depth, phy_regs);

        pr_info("%s with %dHz %d pixel\r\n", __func__, pixel_clock, color_depth);
        if(!test_bit(HDMI_TX_STATUS_PHY_ALIVE, &dev->status)) {
                // 1. i_presetn = 0 (ARESETn set to 1 -> reset)
                VIOC_DDICONFIG_reset_hdmi_phy(dev->ddibus_io, 1);


                // 2. i_presetn = 1 (ARESETn set to 0 -> normal)
                VIOC_DDICONFIG_reset_hdmi_phy(dev->ddibus_io, 0);
        }

        // 3. Config PHY REGS.
        // Clear MODE_SET_DONE
        tcc_hdmi_phy_mode_set_done(dev, 0);

        if(!test_bit(HDMI_TX_STATUS_PHY_ALIVE, &dev->status)) {
                // 4. i_phy_en = 1
                hdmi_phy_gen2_tx_power_on(dev, 1); udelay(1);

                for(phy_loop = 0; phy_loop <  HDMI_PHY_REGS; phy_loop++){
                        phy_offset = tcc_phy_config_addr[phy_loop];
                        phy_val = phy_regs[phy_loop];
                        if(phy_offset == 0x21) {
				/* mode set done */
				continue;
			}
                        hdmi_phy_write(dev, (phy_offset << 2), phy_val);
                }

                dwc_hdmi_phy_dump(dev);

                // 7.   i_phy_rstn = 1
                mc_phy_reset(dev, 1);
        }


        /**
         * The 8-bit I2C slave addresses of the EDID are 0xA0/0xA1 and the address of
         * SCDC are 0xA8/0xA9.
         * I thought that 2k TV would not respond to SCDC address, but I found the 2k TV
         * that responding to the SCDC address. The 2k tv initializes some of the edids when
         * it receives the tmds character ratio or scramble command through the scdc address.
         * Then an edid checksum error will occur when the source reads edid.
         * To prevent this, i changed the source to use scdc address only if the sink
         * supports scdc address. */
        if(scdc_present) {
		int enable_tmds_clock_ratio = 0;
                if(tmds_clock >= 340000000) {
                        enable_tmds_clock_ratio = 1;
                }
                scdc_set_tmds_bit_clock_ratio_and_scrambling(dev, enable_tmds_clock_ratio, enable_scramble);
                if(enable_scramble) {
                        scrambling(dev, 1);
                }
		/* This is required for compatible with specific SINK (eg LG 49UH7600, 49UH9300) */
                mdelay(85);
        }


        // 8. Set MODE_SET_DONE
        tcc_hdmi_phy_mode_set_done(dev, 1);

        // PHY need max 500us to stable.
        usleep_range(500, 1000);

        if(!test_bit(HDMI_TX_STATUS_PHY_ALIVE, &dev->status)) {
                hdmi_activate_callback();
        }

        //dwc_hdmi_phy_mask(dev, 0);
end_process:

        return ret;
}



