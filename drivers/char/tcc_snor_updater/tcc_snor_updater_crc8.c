uint32_t tcc_snor_calc_crc8(u8 *base, uint32_t length)
{
	uint32_t crcout = 0;
	uint32_t cnt;
	u8 code;
	u8 tmp;

	for (cnt = 0; cnt < length; cnt++) {
		code = base[cnt];
		tmp = code^crcout;
		crcout = (crcout >> (uint32_t)8) ^ CRC32_TABLE[tmp & (u8)0xFF];
	}

	return crcout;
}

